// Variables for multiplication
const num1 = 31;
const num2 = 2;
const multiply = num1 * num2; // 31 * 2 = 62

// Generate a random integer greater than 0
const random = Math.floor(Math.random() * 100) + 1; // Random integer from 1 to 100

// Variables for modulus operation
const num3 = 28;
const num4 = 6;
const mod = num3 % num4; // 28 % 6 = 4

// Find the highest number in a set
const max = Math.max(5, 10, 15, 20); // Returns 20

// Output for verification (optional)
console.log("multiply:", multiply);
